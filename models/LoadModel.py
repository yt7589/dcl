    def forward(self, x, last_cont=None, run_mode=RUN_MODE_NORMAL):
        x = self.model(x)
        if self.use_dcl:
            mask = self.Convmask(x)
            mask = self.avgpool2(mask)
            mask = torch.tanh(mask)
            mask = mask.view(mask.size(0), -1)
        x = self.avgpool(x)
        if MainModel.RUN_MODE_FEATURE_EXTRACT == run_mode:
            return x
